#!/usr/bin/env node
// ─── Hindi Meanings Generator ─────────────────────────────────────────────────
// Reads all library entries + Gita verses, batches them 20 at a time,
// calls Gemini flash-lite to translate meanings to Hindi, and writes
// src/lib/i18n/hindi-meanings.ts as a lookup map.
//
// Usage:
//   GEMINI_API_KEY=xxx node scripts/generate-hindi-meanings.mjs
//
// Resume-safe: entries already present in the output file are skipped.
// ─────────────────────────────────────────────────────────────────────────────

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const OUT_FILE = path.join(ROOT, 'src/lib/i18n/hindi-meanings.ts');

const GEMINI_API_KEY = process.env.GEMINI_API_KEY;
if (!GEMINI_API_KEY) {
  console.error('GEMINI_API_KEY not set');
  process.exit(1);
}

const BATCH_SIZE = 20;
const DELAY_MS   = 800; // stay under flash-lite rate limits

// ── Load existing output so we can resume ─────────────────────────────────────
let existingMap = {};
if (fs.existsSync(OUT_FILE)) {
  const content = fs.readFileSync(OUT_FILE, 'utf8');
  const match = content.match(/export const HINDI_MEANINGS[^=]+=\s*(\{[\s\S]*?\});/);
  if (match) {
    try { existingMap = JSON.parse(match[1].replace(/'/g, '"')); } catch {}
  }
}

// ── Extract entries from gita-full-data.ts ────────────────────────────────────
function loadGitaEntries() {
  const raw = fs.readFileSync(path.join(ROOT, 'src/lib/gita-full-data.ts'), 'utf8');
  const entries = [];
  const blocks = raw.split(/\{\s*\n\s*"id":/);
  for (const block of blocks.slice(1)) {
    const id      = block.match(/"([^"]+)"/)?.[1];
    const meaning = block.match(/"meaning":\s*"((?:[^"\\]|\\.)*)"/)?.[1];
    if (id && meaning) {
      entries.push({ id, meaning: meaning.replace(/\\n/g, ' ').replace(/\\"/g, '"') });
    }
  }
  return entries;
}

// ── Extract entries from library-content.ts ───────────────────────────────────
function loadLibraryEntries() {
  const raw = fs.readFileSync(path.join(ROOT, 'src/lib/library-content.ts'), 'utf8');
  const entries = [];
  // Match id: '...' and meaning: '...' pairs — they always appear near each other
  const idRe      = /id:\s*['"]([^'"]+)['"]/g;
  const meaningRe = /meaning:\s*'((?:[^'\\]|\\.)*)'/g;
  const ids = [...raw.matchAll(idRe)].map(m => m[1]);
  const meanings = [...raw.matchAll(meaningRe)].map(m => m[1].replace(/\\'/g, "'"));
  for (let i = 0; i < Math.min(ids.length, meanings.length); i++) {
    entries.push({ id: ids[i], meaning: meanings[i] });
  }
  return entries;
}

// ── Gemini call ───────────────────────────────────────────────────────────────
async function translateBatch(batch) {
  const prompt = `You are translating scripture verse meanings to simple, natural Hindi (Devanagari script).
For each entry below, provide a concise Hindi meaning (2-4 sentences max). Preserve the spiritual depth.
Respond with ONLY a JSON array in this exact format, no extra text:
[{"id":"...","meaning_hi":"..."}]

Entries:
${JSON.stringify(batch.map(e => ({ id: e.id, meaning_en: e.meaning })), null, 2)}`;

  const res = await fetch(
    `https://generativelanguage.googleapis.com/v1beta/models/gemini-2.0-flash-lite:generateContent?key=${GEMINI_API_KEY}`,
    {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        contents: [{ parts: [{ text: prompt }] }],
        generationConfig: { temperature: 0.2, maxOutputTokens: 4096 },
      }),
    }
  );
  if (!res.ok) throw new Error(`Gemini error ${res.status}: ${await res.text()}`);
  const data = await res.json();
  const text = data.candidates?.[0]?.content?.parts?.[0]?.text ?? '';
  // Strip markdown fences
  const cleaned = text.replace(/```json\n?|\n?```/g, '').trim();
  return JSON.parse(cleaned);
}

// ── Write output file ─────────────────────────────────────────────────────────
function writeOutput(map) {
  const entries = Object.entries(map)
    .map(([k, v]) => `  "${k}": ${JSON.stringify(v)},`)
    .join('\n');
  const content = `// ─── Hindi Meanings Lookup ────────────────────────────────────────────────────
// Auto-generated by scripts/generate-hindi-meanings.mjs
// Maps entry ID → Hindi meaning/अर्थ
// DO NOT edit manually — re-run the generation script to update.
// ─────────────────────────────────────────────────────────────────────────────

export const HINDI_MEANINGS: Record<string, string> = {
${entries}
};

/**
 * Look up the Hindi meaning for a library entry by ID.
 * Falls back to undefined if not yet generated.
 */
export function getHindiMeaning(id: string): string | undefined {
  return HINDI_MEANINGS[id];
}
`;
  fs.writeFileSync(OUT_FILE, content, 'utf8');
}

// ── Main ──────────────────────────────────────────────────────────────────────
async function main() {
  const gitaEntries    = loadGitaEntries();
  const libraryEntries = loadLibraryEntries();
  const all = [...gitaEntries, ...libraryEntries];

  const todo = all.filter(e => !existingMap[e.id]);
  console.log(`Total entries: ${all.length} | Already done: ${all.length - todo.length} | To generate: ${todo.length}`);

  const resultMap = { ...existingMap };
  let batchNum = 0;

  for (let i = 0; i < todo.length; i += BATCH_SIZE) {
    const batch = todo.slice(i, i + BATCH_SIZE);
    batchNum++;
    const totalBatches = Math.ceil(todo.length / BATCH_SIZE);
    process.stdout.write(`Batch ${batchNum}/${totalBatches} (${batch[0].id} … ${batch[batch.length-1].id}) … `);

    try {
      const results = await translateBatch(batch);
      for (const r of results) {
        if (r.id && r.meaning_hi) resultMap[r.id] = r.meaning_hi;
      }
      console.log(`✓ ${results.length} translated`);
    } catch (err) {
      console.error(`✗ Failed: ${err.message}`);
      // Save progress so far before bailing
      writeOutput(resultMap);
      process.exit(1);
    }

    writeOutput(resultMap);
    if (i + BATCH_SIZE < todo.length) await new Promise(r => setTimeout(r, DELAY_MS));
  }

  console.log(`\nDone. ${Object.keys(resultMap).length} Hindi meanings written to ${OUT_FILE}`);
}

main().catch(err => { console.error(err); process.exit(1); });
